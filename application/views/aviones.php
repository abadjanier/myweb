                        <p>Reconocido unánimamente como uno de los mejores cazabombarderos construidos, el Phantom fue durante muchos años el máximo exponente del concepto, tan en boga hoy día, de avión polivalente. Su proyecto arrancó en 1953 con la convocatoria por la US Navy de un programa para adquirir un nuevo reactor embarcado que fuera un compromiso entre velocidad, maniobrabilidad, capacidad de carga bélica, peso y potencia. El producto que presentó la McDonnell, tras el estudio de muchos proyectos y de miles de horas de pruebas, reunía unas cualidades tan evidentes que la US Navy, tras la evaluación de dos prototipos, encargó de inmediato su producción en serie. El potencial del nuevo avión quedó demostrado en seguida con la consecución de una serie de plusmarcas, la primera de ellas el récord mundial absoluto de altitud con 30.040 m logrado por el prototipo el 6 de diciembre de 1959. Más tarde el Phantom conseguiría 14 nuevos récords, entre ellos el mundial absoluto de velocidad con 2.585 km./h conseguido el 22 de noviembre de 1961.

En su dilatada carrera se fabricaron un total de 5.201 unidades, construidas en diferentes versiones, siendo las principales las F-4B de la Navy, la F-4C de la USAF, la F-4E para la exportación, la F-4K para la Royal Navy y la RF-4C de reconocimiento fotográfico. Ha participado en todos los grandes conflictos de finales del siglo XX desde la guerra del Vietnam hasta las de Oriente Próximo, sirviendo en las fuerzas aéreas de numerosos países, entre ellos España a donde llegaron el 14 de junio de 1971. Los F-4C se encuadraron en el Ala 12 formando dos escuadrones, el 121 “Póker” y el 122 “Tenis”, mientras que con los RF-4C se creó el Escuadrón de Reconocimiento 123 “Titán”. Su llegada supuso para nuestro país el disponer por primera vez de un material moderno y de primera línea, significando un gran salto cualitativo para nuestro Ejército del Aire. Los Phantom estuvieron prestando un excelente servicio hasta su sustitución por los nuevos F-18 Hornet. El 13 de septiembre de 1990 fueron dados de baja los F-4C, mientras que los RF-4C de reconocimiento han continuado en activo hasta finales del año 2002.

El F-4C de nuestra colección, el 122-13 llegó a Sabadell desmontado y en dos grandes camiones. Actualmente está en proceso de
restauración y montaje.</p>
